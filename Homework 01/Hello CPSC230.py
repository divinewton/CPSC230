# README
# This script is an example of creating responses to user's inputs through questions.
# Users are asked for their name and favorite movie, and recieve responses.
# -----------------------------------------------

# Says hello
print("Hello friend!")

# asks user for name
print ("What is your name?")
name = input()

# says hello with user's name
print("Hello " + name + ". What a lovely name!")

# asks user's favorite movie
print ("What's your favorite movie?")
movie = input()

# responds to user's favorite movie with agreement
print("That's such a good movie, " + name + "! " + movie + " is one of my favorites as well!")